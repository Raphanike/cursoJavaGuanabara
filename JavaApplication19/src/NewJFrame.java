    private void CalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CalcularActionPerformed
        // TODO add your handling code here:
        panCalc.setVisible(true);
        int valorDigitado = (Integer) Valor.getValue();
        int resto = valorDigitado % 2;
        double resto2 = Math.pow(valorDigitado, 3);
        double resto3 = Math.sqrt(valorDigitado);
        double resto4 = Math.cbrt(valorDigitado);
        double resto5 = Math.abs(valorDigitado);
        
        DecimalFormat df = new DecimalFormat("#.###");
        
        resDiv.setText(String.valueOf(resto));
        resCub.setText(df.format(resto2));
        resQua.setText(df.format(resto3));
        resCubica.setText(df.format(resto4));
        resAb.setText(df.format(resto5));
    }//GEN-LAST:event_CalcularActionPerformed

